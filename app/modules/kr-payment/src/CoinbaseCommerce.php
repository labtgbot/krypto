      $this->CoinbaseCommerce->setApiKey($this->_getApp()->_getCoinbaseCommerceAPIKey());
    }

    public function _getCoinbaseCommerce(){
      if(is_null($this->CoinbaseCommerce)) $this->_init();
      return $this->CoinbaseCommerce;
    }

