    public function _createNewPayment($User, $Amount, $BalanceUser, $Currency){

      $Currency = (in_array($Currency, $this->_getCurrencyAvailable()) ? $Currency : $this->_getCurrencyAvailable()[0]);

      $charge = new \WPDMPP\Coinbase\Commerce\Model\Charge();

      $money = new \WPDMPP\Coinbase\Commerce\Model\Money();
      $money->SetAmount($Amount);
      $money->SetCurrency($Currency);

      $DepositReference = $BalanceUser->_addDeposit($Amount, 'coinbasecommerce', 'Payment Coinbase Commerce '.$Amount.' '.$Currency, $Currency, "-not-filled-yet-".App::encrypt_decrypt('encrypt', time().rand(0, 9999999).time()), 0);

      $charge->setName($this->_getApp()->_getCoinbaseCommercePaymentTitle());
      $charge->setDescription($DepositReference.' - '.$this->_getApp()->_formatNumber($Amount, 2).' '.$Currency.' - Deposit');
      $charge->setPricingType('fixed_price');
      $charge->setLocalPrice($money);
      $charge->addMetadata('id_user', $User->_getUserID());


      $charge->addMetadata('deposit_reference', $DepositReference);

      $charge->setRedirectUrl(APP_URL.'/app/modules/kr-payment/src/actions/deposit/processCoinbaseCommerce.php');

      $response = $this->_getCoinbaseCommerce()->createCharge($charge);

      $responseDecoded = json_decode($response);
      $BalanceUser->_updateDepositPaymentData($DepositReference, $responseDecoded->data->id);

      return $responseDecoded->data->hosted_url;

    }

    public function _parseWebhook($payload, $rawBody = null, $signature = null){
      if(is_null($rawBody)) $rawBody = file_get_contents('php://input');
      if(is_null($signature) && array_key_exists('HTTP_X_CC_WEBHOOK_SIGNATURE', $_SERVER)) $signature = $_SERVER['HTTP_X_CC_WEBHOOK_SIGNATURE'];

      $processor = new CoinbaseCommerceDepositProcessor($this);
      return $processor->_processWebhook($payload, $rawBody, $signature, $this->_getApp()->_getCoinbaseCommerceWebhookSecret());
    }

    public function _confirmTransaction($payload){
      $processor = new CoinbaseCommerceDepositProcessor($this);
      return $processor->_confirmTransaction($payload);
    }

    public function _validateRequest($payload, $rawBody = null, $signature = null){
      if(is_null($rawBody)) $rawBody = file_get_contents('php://input');
      if(is_null($signature) && array_key_exists('HTTP_X_CC_WEBHOOK_SIGNATURE', $_SERVER)) $signature = $_SERVER['HTTP_X_CC_WEBHOOK_SIGNATURE'];

      CoinbaseCommerceDepositProcessor::_validateSignature($rawBody, $signature, $this->_getApp()->_getCoinbaseCommerceWebhookSecret());
      CoinbaseCommerceDepositProcessor::_eventType($payload);
      return true;
    }

    public function _findProcessedCoinbaseCommerceDeposit($chargeId, $userId){
      $r = parent::querySqlRequest("SELECT * FROM deposit_history_krypto
                                    WHERE id_user=:id_user
                                      AND payment_type_deposit_history=:payment_type_deposit_history
                                      AND payment_status_deposit_history<>:pending_status
                                      AND payment_data_deposit_history=:payment_data_deposit_history
                                    ORDER BY id_deposit_history DESC LIMIT 1",
                                  [
                                    'id_user' => $userId,
                                    'payment_type_deposit_history' => 'coinbasecommerce',
                                    'pending_status' => '0',
                                    'payment_data_deposit_history' => $chargeId
                                  ]);

      return (count($r) > 0 ? $r[0] : null);
    }

    public function _findPendingCoinbaseCommerceDeposit($chargeId, $userId, $depositReference){
      $r = parent::querySqlRequest("SELECT * FROM deposit_history_krypto
                                    WHERE id_user=:id_user
                                      AND ref_deposit_history=:ref_deposit_history
                                      AND payment_type_deposit_history=:payment_type_deposit_history
                                      AND payment_status_deposit_history=:payment_status_deposit_history
                                      AND payment_data_deposit_history=:payment_data_deposit_history
                                    ORDER BY id_deposit_history DESC LIMIT 1",
                                  [
                                    'id_user' => $userId,
                                    'ref_deposit_history' => $depositReference,
                                    'payment_type_deposit_history' => 'coinbasecommerce',
                                    'payment_status_deposit_history' => '0',
                                    'payment_data_deposit_history' => $chargeId
                                  ]);

      return (count($r) > 0 ? $r[0] : null);
    }

    public function _confirmCoinbaseCommerceDeposit($deposit, $status){
      if(!is_array($deposit) || !array_key_exists('id_deposit_history', $deposit)) return false;

      $req = self::getSqlConnexion()->prepare("UPDATE deposit_history_krypto
                                                SET payment_status_deposit_history=:payment_status_deposit_history
                                                WHERE id_deposit_history=:id_deposit_history
                                                  AND id_user=:id_user
                                                  AND ref_deposit_history=:ref_deposit_history
                                                  AND payment_type_deposit_history=:payment_type_deposit_history
                                                  AND payment_status_deposit_history=:pending_status
                                                  AND payment_data_deposit_history=:charge_id");
      $ok = $req->execute([
        'payment_status_deposit_history' => (string) $status,
        'id_deposit_history' => $deposit['id_deposit_history'],
        'id_user' => $deposit['id_user'],
        'ref_deposit_history' => $deposit['ref_deposit_history'],
        'payment_type_deposit_history' => 'coinbasecommerce',
        'pending_status' => '0',
        'charge_id' => $deposit['payment_data_deposit_history']
      ]);
      $updatedRows = $req->rowCount();
      $req->closeCursor();

      return $ok && $updatedRows === 1;
    }


}
