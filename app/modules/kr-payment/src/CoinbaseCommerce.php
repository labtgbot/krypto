class CoinbaseCommerceDepositProcessor {

  private $repository;

  public function __construct($repository){
    $this->repository = $repository;
  }

  public function _processWebhook($payload, $rawBody, $signature, $webhookSecret){
    self::_validateSignature($rawBody, $signature, $webhookSecret);

    $eventType = self::_eventType($payload);
    if($eventType == 'charge:confirmed'){
      return $this->_confirmTransaction($payload);
    }

    return [
      'status' => 'ignored',
      'event_type' => $eventType
    ];
  }

  public function _confirmTransaction($payload){
    $payment = self::_validatedConfirmedCharge($payload);

    $processed = $this->repository->_findProcessedCoinbaseCommerceDeposit($payment['charge_id'], $payment['user_id']);
    if(!is_null($processed)){
      return [
        'status' => 'duplicate',
        'charge_id' => $payment['charge_id']
      ];
    }

    $deposit = $this->repository->_findPendingCoinbaseCommerceDeposit($payment['charge_id'], $payment['user_id'], $payment['deposit_reference']);
    if(!is_array($deposit)) throw new Exception('Coinbase Commerce pending deposit not found.', 1);

    self::_assertDepositMatchesPayment($deposit, $payment);

    $confirmed = $this->repository->_confirmCoinbaseCommerceDeposit($deposit, '1');
    if(!$confirmed) throw new Exception('Coinbase Commerce deposit confirmation failed.', 1);

    return [
      'status' => 'confirmed',
      'charge_id' => $payment['charge_id']
    ];
  }

  public static function _expectedSignature($rawBody, $webhookSecret){
    return hash_hmac("sha256", (string) $rawBody, (string) $webhookSecret);
  }

  public static function _validateSignature($rawBody, $signature, $webhookSecret){
    $secret = trim((string) $webhookSecret);
    if($secret === '') throw new Exception('Coinbase Commerce webhook secret is not configured.', 1);
    if(!is_scalar($signature) || trim((string) $signature) === '') throw new Exception("Webhook signature not included in the headers of the request.", 1);

    $hash = self::_expectedSignature((string) $rawBody, $secret);
    if(!hash_equals($hash, trim((string) $signature))) {
      throw new Exception("The webhook signature of the request does not match the one generated by the server.", 1);
    }

    return true;
  }

  public static function _eventType($payload){
    if(!is_array($payload) || !array_key_exists('event', $payload) || !is_array($payload['event'])) {
      throw new Exception("Request needs to contain 'event' value.", 1);
    }

    return self::_requiredScalar($payload['event'], 'type', 'Coinbase Commerce event type');
  }

  private static function _validatedConfirmedCharge($payload){
    $eventType = self::_eventType($payload);
    if($eventType !== 'charge:confirmed') throw new Exception('Coinbase Commerce event is not a confirmed charge.', 1);

    $event = $payload['event'];
    if(!array_key_exists('data', $event) || !is_array($event['data'])) throw new Exception("Error Coinbase Commerce : Invalid payment", 1);
    if(!array_key_exists('metadata', $event['data']) || !is_array($event['data']['metadata'])) throw new Exception("Error Coinbase Commerce : Invalid payment", 1);

    $payment = [
      'event_id' => self::_requiredScalar($event, 'id', 'Coinbase Commerce event id'),
      'charge_id' => self::_requiredScalar($event['data'], 'id', 'Coinbase Commerce charge id'),
      'deposit_reference' => self::_requiredScalar($event['data']['metadata'], 'deposit_reference', 'Coinbase Commerce deposit reference'),
      'user_id' => self::_requiredScalar($event['data']['metadata'], 'id_user', 'Coinbase Commerce user id')
    ];

    self::_assertProviderIdentifier($payment['event_id'], 'Coinbase Commerce event id');
    self::_assertProviderIdentifier($payment['charge_id'], 'Coinbase Commerce charge id');
    self::_assertProviderIdentifier($payment['deposit_reference'], 'Coinbase Commerce deposit reference');

    if(!preg_match('/^[0-9]{1,20}$/', $payment['user_id'])){
      throw new Exception('Coinbase Commerce user id is invalid.', 1);
    }

    return $payment;
  }

  private static function _requiredScalar($data, $field, $label){
    if(!array_key_exists($field, $data) || !is_scalar($data[$field])){
      throw new Exception($label.' is missing.', 1);
    }

    $value = trim((string) $data[$field]);
    if($value === '') throw new Exception($label.' is empty.', 1);
    return $value;
  }

  private static function _assertProviderIdentifier($value, $label){
    if(strlen((string) $value) > 191 || preg_match('/[\x00-\x1F\x7F]/', (string) $value)){
      throw new Exception($label.' is invalid.', 1);
    }
  }

  private static function _assertDepositMatchesPayment($deposit, $payment){
    if((string) $deposit['id_user'] !== (string) $payment['user_id']){
      throw new Exception('Coinbase Commerce deposit user mismatch.', 1);
    }

    if(strtolower((string) $deposit['payment_type_deposit_history']) !== 'coinbasecommerce'){
      throw new Exception('Coinbase Commerce deposit type mismatch.', 1);
    }

    if((string) $deposit['payment_status_deposit_history'] !== '0'){
      throw new Exception('Coinbase Commerce deposit is not pending.', 1);
    }

    if((string) $deposit['payment_data_deposit_history'] !== (string) $payment['charge_id']){
      throw new Exception('Coinbase Commerce charge id mismatch.', 1);
    }

    if((string) $deposit['ref_deposit_history'] !== (string) $payment['deposit_reference']){
      throw new Exception('Coinbase Commerce deposit reference mismatch.', 1);
    }
  }
}

class CoinbaseCommerce extends MySQL
